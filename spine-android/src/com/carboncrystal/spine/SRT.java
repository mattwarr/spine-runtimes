package com.carboncrystal.spine;

public class SRT {
	public float x;
	public float y;
	public float rotation;
	public float scaleX;
	public float scaleY;

	@Override
	public String toString() {
		return "SRT{" +
				"x=" + x +
				", y=" + y +
				", rotation=" + rotation +
				", scaleX=" + scaleX +
				", scaleY=" + scaleY +
				'}';
	}
}
